let marks = 90;

// Using ternary operator
(marks > 80) ? console.log("pass") :
    (marks == 90) ? console.log("your grade A++") :
    console.log("fail");

// Using if-else if-else
if (marks > 80) {
    console.log("pass");
} else if (marks == 90) {
    console.log("your grade A+");
} else {
    console.log("fail");
}
